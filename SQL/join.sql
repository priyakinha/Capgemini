       EMPLOYEE.EMP E1,EMPLOYEE.EMP E2 
       WHERE E1.MGR=E2.EMPNO AND (E2.SAL>E1.SAL) AND(E1.DEPTNO=10 OR E1.DEPTNO=20);
       
-- 11
